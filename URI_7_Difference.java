package August_Intersting_Uri;

import java.util.Scanner;

public class URI_7_Difference {
    public static void main(String[] args) {
        int a, b, c, d, df;
        Scanner sc = new Scanner(System.in);
        a = sc.nextInt();
        b = sc.nextInt();
        c = sc.nextInt();
        d = sc.nextInt();
        df = (a * b - c * d);
        System.out.println("DIFERENCA = "+df);

    }
}
