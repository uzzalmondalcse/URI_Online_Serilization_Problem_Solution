package August_Intersting_Uri;

import java.util.Scanner;

public class URI_3_SimpleSum {
    public static void main(String[] args) {
        int a, b, SOMA;
        Scanner sc = new Scanner(System.in);
        a = sc.nextInt();
        b = sc.nextInt();
        SOMA = a + b;
        System.out.println("SOMA = "+SOMA);
    }
}
