package August_Intersting_Uri;

import java.util.Scanner;

public class URI_4_SimpleProduct {
    public static void main(String[] args) {
        int a, b, PROD;
        Scanner sc = new Scanner(System.in);
        a = sc.nextInt();
        b = sc.nextInt();
        PROD = a * b;
        System.out.println("PROD = "+PROD);
    }
}
